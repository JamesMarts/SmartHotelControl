    public static String GetCrc16(String str) {
        byte[] bytes = str.getBytes();
        int crc = 0x0000;
        for (byte b : bytes) crc = (crc >>> 8) ^ CRC16Table[(crc ^ b) & 0xff];
        Log.e("GetCrc16 =","GetCrc16 ="+Integer.toHexString(crc));
        return Integer.toHexString(crc);
    }

    public static String GetCrc162(String str) { //writed by jamky
        byte[] bytes = str.getBytes();
        int crc = 0x0000;

        for (byte b : bytes) crc = (crc << 8) ^ CRC16Table[(crc >> 8) ^ b]; //(b & 0xff)
        return Integer.toHexString(crc);
    }


    /**
     * 将两个ASCII字符合成一个字节；
     * 如："EF"--> 0xEF
     *
     * @param src0 byte
     * @param src1 byte
     * @return byte
     */
    public static byte uniteBytes(byte src0, byte src1) {
        byte _b0 = Byte.decode("0x" + new String(new byte[]{src0})).byteValue();
        _b0 = (byte) (_b0 << 4);
        byte _b1 = Byte.decode("0x" + new String(new byte[]{src1})).byteValue();
        byte ret = (byte) (_b0 ^ _b1);
        return ret;
    }

